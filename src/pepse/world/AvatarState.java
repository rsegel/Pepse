package pepse.world;

/**
 * The AvatarState enum represents the different states the avatar can be in.
 * The avatar can be idle, running or jumping.
 */
public enum AvatarState {
    /**
     * The avatar is idle.
     */
    IDLE,
    /**
     * The avatar is running.
     */
    RUN,
    /**
     * The avatar is jumping.
     */
    JUMP
}
